/* ***********************************************************************
  @fn             VLCVehFillErrorOut */ /*!

                                     @brief          Fill VLC error output
                                   structure

                                     @description    Fill VLC_VEH error output
                                   structure

                                     @param[out]     pDest : the error output
                                   structure to fill

                                     @return         void

                                   ****************************************************************************
                                   */
void VLCVehFillErrorOut(VLC_DFVErrorOut_t* pDest) {
    pDest->uiVersionNumber = VLC_VEH_INTFVER;
    /* Set output states */
    pDest->YawRateUnplausible = AL_ERR_STATE_UNKNOWN;
    pDest->VehicleSpeedUnplausible = AL_ERR_STATE_UNKNOWN;
}

